function processC(docs: LiturgicalDocument[], version: string) {
  for (const doc of docs) {
    const modified = {
      ...doc,
      version,
      category: ["Canticle"],
      sharing: undefined,
      date_created: undefined,
      date_modified: undefined,
    };

    const numIndex = parseInt((doc as any).slug.split("canticle-")[1]),
      eowRepeat =
        version === "EOW" &&
        !Number.isNaN(parseInt((doc as any).slug.split("canticle-")[1])),
      alphaIndex =
        Number.isNaN(numIndex) &&
        (doc as any).slug.split("canticle-")[1].charCodeAt(0),
      index = (numIndex || alphaIndex) * 100 + (eowRepeat ? 1 : 0);

    Deno.writeTextFile(
      `${(doc as any).slug}${eowRepeat ? "-eow" : ""}.json`,
      JSON.stringify({
        index,
        data: [modified],
      })
    );
  }
}

processC(
  R1.map((d) => new LiturgicalDocument(d)),
  "Rite-I"
);
processC(
  R2.map((d) => new LiturgicalDocument(d)),
  "Rite-II"
);
processC(
  EOW.map((d) => new LiturgicalDocument(d)),
  "EOW"
);
